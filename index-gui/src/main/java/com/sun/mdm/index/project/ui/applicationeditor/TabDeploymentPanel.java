    public String getTransaction() {
        int i = jComboBoxTransactions.getSelectedIndex();
        String transaction = "CONTAINER"; // NOI18N
        if (i == 1) {
            transaction = "BEAN"; // NOI18N
        } else if (i == 2) {
            transaction = "LOCAL"; // NOI18N
        }
        return transaction;
    }
