    /** Creates new form TabDeploymentPanel */
    public TabDeploymentPanel(EviewEditorMainApp eviewEditorMainApp, MasterType masterType) {
        initComponents();
        mEviewEditorMainApp = eviewEditorMainApp;
        mMasterType = masterType;
        jComboBoxTransactions.insertItemAt(TRANSACTION_CONTAINER, 0);
        jComboBoxTransactions.insertItemAt(TRANSACTION_BEAN, 1);
        jComboBoxTransactions.insertItemAt(TRANSACTION_LOCAL, 2);

        String transaction = mMasterType.getTransaction();
        int index = 0;  // default is "CONTAINER"
        if (transaction != null) {
            if (transaction.equals("BEAN")) { // NOI18N
                index = 1;
            } else if (transaction.equals("LOCAL")) { // NOI18N
                index = 2;
            }
        } 
        jComboBoxTransactions.setSelectedIndex(index);
        jComboBoxTransactions.addItemListener(new java.awt.event.ItemListener() {
            public void itemStateChanged(java.awt.event.ItemEvent evt) {
                mMasterType.setTransaction(getTransaction());
                mMasterType.setModified(true);
                mEviewEditorMainApp.enableSaveAction(true);
            }
        });
    }
    
