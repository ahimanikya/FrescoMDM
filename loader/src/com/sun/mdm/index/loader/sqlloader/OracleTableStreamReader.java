package com.sun.mdm.index.loader.sqlloader;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.util.logging.Logger;

/**
 * @author Sujit Biswas
 * 
 */
public class OracleTableStreamReader extends Thread {

	private static Logger logger = Logger.getLogger(OracleTableLoader.class
			.getName());

	private InputStream is;
	private String type;
	private String tableName;

	public OracleTableStreamReader(String tableName, InputStream is, String type) {
		this.is = is;
		this.type = type;
		this.tableName=tableName;
	}

	public void run() {
		try {
			InputStreamReader isr = new InputStreamReader(is);
			BufferedReader br = new BufferedReader(isr);
			String line = null;
			while ((line = br.readLine()) != null) {
				logger.finest(tableName + "-"+type + ":" + line);
			}
		} catch (IOException ioe) {
			ioe.printStackTrace();
		}
	}
}
